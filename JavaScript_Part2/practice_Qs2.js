// Create a System to calculate popcorn prices based on the size customer asked for:

let popcorn_Size = 'XL';

if(popcorn_Size === 'XL'){
    console.log("if size is 'XL', price is RS.250");
}else if(popcorn_Size === 'L'){
    console.log("if size is 'L', price is Rs.200");
}else if(popcorn_Size === 'M'){
    console.log("if size is 'M', price is Rs.100");
}else if(popcorn_Size === 'S'){
    console.log("if size is 'S', price is Rs.50");
}else{
    console.log("this size and price is not available")
}
// Write a switch statement to print the month in a quarter.

let Month = 3;

switch(Month){
    case 1:
        console.log(`Jan,Feb,March`);
        break;
    case 2:
        console.log(`April, May, June`);
        break;
    case 3:
        console.log(`July, August, September`);
        break;
    case 4:
        console.log(`October, November, December`);
        break;
    default:
        console.log(`Sorry Invalid Number`)
        
}
// Switch Statement
// Used when we have some fixed values that we need to compare to.

let color = "blue";

switch(color){
    case  "red" :
        console.log("Stop");
        break;
    case "yellow" :
        console.log("Slow down");
        break;
    case "Green" :
        console.log("GO");
        break;
    default :
        console.log("Broken Light");
}